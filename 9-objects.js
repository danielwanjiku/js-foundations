//what are objects in javascript?
//Objects are a fundamental data structure in JavaScript that allow us to store and organize data in a structured way. 
//An object is a collection of key-value pairs, where each key is a string (or symbol) and each value can be any data type, including other objects. 
//Objects are defined using curly braces {} and can contain properties and methods.
//Example of an object:
let person = {
    name: "John",
    age: 30,
    city: "New York"
};

//why are objects important?
//Objects are important because they allow us to represent real-world entities and their attributes in our code. 
//They provide a way to group related data together and perform operations on that data using methods. 
//Objects also allow us to create complex data structures and models, which can be used to build more sophisticated applications. 

//how are objects executed in javascript?
//When an object is created, the JavaScript engine allocates memory for the object and initializes it with the specified properties and values. 
//The properties of the object can be accessed using dot notation or bracket notation, and methods can be called on the object to perform operations on its data. 
//examples of how to access properties and call methods on an object:
let person = {
    name: "John",
    age: 30,
    city: "New York"
};
console.log(person.name); // Output: John
console.log(person.age); // Output: 30
console.log(person.city); // Output: New York


//When we perform operations on the object, such as adding or modifying properties, the JavaScript engine updates the object accordingly and manages the memory allocation as needed. 
//Overall, objects are executed in a way that allows us to efficiently store and manipulate structured data in our JavaScript programs.